class AccessedIfArray {
    public static void main(String[] args) {
        int[] numbers = {11, 22, 33, 44, 55, 66, 77, 88, 99, 100}; 

        for (int i = 0; i < numbers.length; i++) { 
            System.out.println("index of element " + i + " & element is = " + numbers[i]);
        }
    }
}